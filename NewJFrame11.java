    private void jButtonResumenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResumenActionPerformed
        //declaracion de variables 
        String a="", e="";
        double b=0, c=0, ss=0, cont=0, d=0, f=0, mayor=-1, menor=9999999;
        //iniciando el proceso repetitivo para recorrer el vector
        for(int i=0;i<NumEmp;i++ )
        {// a) nombre del empleado que tenga el mayor sueldo neto
            if (ArregloEmp[i].sueldoNeto()> mayor)
            { mayor = ArregloEmp[i].sueldoNeto();
                a = ArregloEmp[i].nombre;
            }
            //b) promedio de sueldos netos del area de marketing
            if(ArregloEmp[i].area.equals("Marketing"))
            { ss=ss+ArregloEmp[i].sueldoNeto();
                cont++;
                b=ss/cont;
            }
            
            //c) numero de empleados cuyp monto de seguro superen ls 100 so
            if(ArregloEmp[i].montoSeguro()>100)
                c++;
            
            //d) numero de empleados del area de sistemas cuyo monto
            //   de horas extra estan entre 500 y 800 so
            if(ArregloEmp[i].area.equals("Sistemas")&& ArregloEmp[i].montoHExtras()>=500
                && ArregloEmp[i].montoHExtras()<=800)
            d++;
            
            //e) empleado snp cpn el menor onto de descuento
            if(ArregloEmp[i].tipo_seg.equals("SNP")&& ArregloEmp[i].montoDescuentos()<menor){
                menor = ArregloEmp[i].montoDescuentos();
                e = ArregloEmp[i].nombre;
            }
            
            //f) total de gastos de la empresa en pagar empleados
            f = f+ ArregloEmp[i].sueldoBruto();
        }
        
        //datos salida
        jTextPane2.setText(a);
        jTextPane3.setText(String.valueOf(b));
        jTextPane4.setText(String.valueOf(c));
        jTextPane5.setText(String.valueOf(d));
        jTextPane6.setText(e);
        jTextPane7.setText(String.valueOf(f));
    }//GEN-LAST:event_jButtonResumenActionPerformed

