    void InsertarEnTabla(Empleado11 UnEmp){
        //dado formato para los datos numericos
        DecimalFormat df2= new DecimalFormat("####.00");
        //generando los datos con formato numerico
        String me=df2.format(UnEmp.montoHExtras());
        String ms=df2.format(UnEmp.montoSeguro());
        String mes=df2.format(UnEmp.montoEssalud());
        String sn=df2.format(UnEmp.sueldoNeto());
        //creando el arreglo de objetos para la tabla 
        Object [] fila={NumEmp+1,UnEmp.codigo,UnEmp.nombre,UnEmp.area,UnEmp.sueldoBase,UnEmp.horasExtras,
            UnEmp.tipo_seg,me,ms,mes,sn};
        // Agregando nuevos datos a la tabla 
        ModeloTabla.addRow(fila);
        }
