    private void jButtonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabarActionPerformed
        //capturar la infomacion de los empleados de los obejtos del formulario
        String cod=jTexCodigo.getText();
        String nom=jTextNombre.getText();
        String seguro=jComboBoxSeguro.getSelectedItem().toString();
        double sueldo=Double.parseDouble(jTextSueldoBase.getText());
        int h_extras=Integer.parseInt(jTextHorasExtra.getText());
        String area=jComboBoxArea.getSelectedItem().toString();
        //crando empleado
        UnEmp =new Empleado11(cod,nom,area,sueldo,h_extras,seguro);
        //colocando el empleado en una posicion
        ArregloEmp[NumEmp]=UnEmp;
        //agregando un empleado a la tabla
        InsertarEnTabla(UnEmp);
        //incrementando el contador en 1
        NumEmp++;
        //Limpiando las entradas
        LimpiarEntradas();
    }//GEN-LAST:event_jButtonGrabarActionPerformed

